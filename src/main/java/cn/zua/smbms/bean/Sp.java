package cn.zua.smbms.bean;

import java.util.Date;

public class Sp {
    private long id;
    private String spName;
    private String spJg;
    private String spCd;
    private String spSsqk;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getSpName() {
        return spName;
    }

    public void setSpName(String spName) {
        this.spName = spName;
    }

    public String getSpJg() {
        return spJg;
    }

    public void setSpJg(String spJg) {
        this.spJg = spJg;
    }

    public String getSpCd() {
        return spCd;
    }

    public void setSpCd(String spCd) {
        this.spCd = spCd;
    }

    public String getSpSsqk() {
        return spSsqk;
    }

    public void setSpSsqk(String spSsqk) {
        this.spSsqk = spSsqk;
    }
}
